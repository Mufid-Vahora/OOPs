class Primitive_datatypes
{
	public static void main (String args[])
	{
		int a;
		char b;
		double c;
		boolean d;
		a=4;
		b='s';
		c=4.5;
		d=true;
		
		System.out.println("The value of Integer Variable is "+a);
		System.out.println("The value of Character Variable is "+b);
		System.out.println("The value of Double Variable is "+c);
		System.out.println("The value of Boolean Variable is "+d);
	}
}